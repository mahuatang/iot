package com.soco.car.app.exception;

/**
 * ClassName:ParamsException <br/>
 * Reason: 参数错误异常. <br/>
 * Date: 2018年7月24日 下午1:30:44 <br/>
 * 
 * @author sunlangping
 * @version
 * @see
 */
public class ParamsException extends RuntimeException {

	private static final long serialVersionUID = -4417244739957488726L;

	public ParamsException() {
		super();
	}

	public ParamsException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ParamsException(String message, Throwable cause) {
		super(message, cause);
	}

	public ParamsException(String message) {
		super(message);
	}

	public ParamsException(Throwable cause) {
		super(cause);
	}

}
